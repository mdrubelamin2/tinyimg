export function muxAv1ToAvif(av1Data: Uint8Array): ArrayBuffer {
  const ftypBox = new Uint8Array([
    0x00, 0x00, 0x00, 0x1c,
    0x66, 0x74, 0x79, 0x70,
    0x61, 0x76, 0x69, 0x66,
    0x00, 0x00, 0x00, 0x00,
    0x61, 0x76, 0x69, 0x66,
    0x6d, 0x69, 0x66, 0x31,
    0x6d, 0x69, 0x61, 0x66,
    0x4d, 0x41, 0x31, 0x42
  ]);
  
  const totalLength = ftypBox.length + av1Data.length;
  const result = new Uint8Array(totalLength);
  
  result.set(ftypBox, 0);
  result.set(av1Data, ftypBox.length);
  
  return result.buffer;
}
